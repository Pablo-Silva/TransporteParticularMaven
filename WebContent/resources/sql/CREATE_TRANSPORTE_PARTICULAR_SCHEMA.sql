ALTER TABLE VEICULO_TABLE ADD CONSTRAINT VEICULO_PK PRIMARY KEY (ID_VEICULO);

ALTER TABLE VEICULO_TABLE ADD CONSTRAINT VEICULO_FK FOREIGN KEY (ID_MOTORISTA) REFERENCES MOTORISTA_TABLE (ID_MOTORISTA);

CREATE TABLE VIAGEM_TABLE (
