CREATE TABLE PESSOA_TABLE (
  ID_PESSOA NUMERIC(10) NOT NULL ,
  PRIMEIRO_NOME VARCHAR(25) NOT NULL,
  SOBRE_NOME VARCHAR(50) NULL,
  DATA_NASCIMENTO DATE NULL,
  SEXO CHAR(1) NULL,
  CPF VARCHAR(15) NOT NULL,
  NUMERO_CELULAR VARCHAR(15) NOT NULL,
  EMAIL VARCHAR(100) NULL
);

ALTER TABLE PESSOA_TABLE ADD CONSTRAINT PESSOA_PK PRIMARY KEY (ID_PESSOA);

CREATE TABLE CLIENTE_TABLE (
  ID_CLIENTE NUMERIC(10) NOT NULL,
  ID_PESSOA NUMERIC(10) NOT NULL,
  BANDEIRA_CARTAO VARCHAR(25) NULL,
  NUMERO_CARTAO NUMERIC(25) NULL
);

ALTER TABLE CLIENTE_TABLE ADD CONSTRAINT CLIENTE_PK PRIMARY KEY (ID_CLIENTE);

ALTER TABLE CLIENTE_TABLE ADD CONSTRAINT CLIENTE_FK FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA_TABLE (ID_PESSOA);

CREATE TABLE MOTORISTA_TABLE (
  ID_MOTORISTA NUMERIC(10) NOT NULL,
  ID_PESSOA NUMERIC(10) NOT NULL,
  DATA_CADASTRO DATE NOT NULL,
  STATUS_MOTORISTA CHAR(1) NOT NULL,
