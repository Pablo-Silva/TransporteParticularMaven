ALTER TABLE MOTORISTA_TABLE ADD CONSTRAINT MOTX_FK FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA_TABLE (ID_PESSOA);

CREATE TABLE VEICULO_TABLE (
